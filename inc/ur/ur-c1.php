        <h4>White Supremacy in the 1920’s</h4>
        <p>The predominant idea of “white supremacy” was threatened by recent activity giving other races the right to vote. Native Americans were given the right to vote in 1924 by the BIA, Bureau of Indian Affairs. Following the new law, Virginia made racially-targeted laws to limit the new freedoms of Native Americans. Virginia’s Racial Integrity Act of 1924.</p>
