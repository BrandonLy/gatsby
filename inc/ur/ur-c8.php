        <h3>Atlantic City</h3>
        <p>Text excerpt: <i>“You ought to go away,” I said. “It’s pretty certain they’ll trace your car.”</br>
    “Go away NOW, old sport?”</br>
“Go to Atlantic City for a week, or up to Montreal.”</i> -Nick Carraway to Jay Gatsby</p>
        <p>Explanation of relation to history: Due to Gatsby’s mistake, Nick suggests that he should go to Atlantic City, which at the time, was when many crimes and secret illegal activity spontaneously occurred during this time. Prohibition, gambling, and organized crime thrived as local authorities did not enforce the law. Overall, Nick makes this comment about Atlantic City as it was a possibility for escape.</p>
        <p>Additional research on topic: Americans at the time went to "speakeasies" or secret clubs that sold alcohol to the public. Local gangs and mafias controlled a great ordeal of smuggling in town. Due to its location near shore, smugglers were able to easily sneak alcohol in Atlantic City.</p>
